package com.wordpress.sreeharshasite;

public class Pattern99 {
	
	public static void main(String[] args)
	 
	{
	 
	int counter=0;
	 
	for(int i=1;i<=7;i++)
	 
	{
	 
	counter=i;
	 
	for(int j=1;j<=7;j++)
	 
	{
	 
	if(i>j)
	 
	{
	 
	System.out.print(" ");
	 
	}
	 
	else
	 
	{
	 
	System.out.print(counter+" ");
	 
	counter++;
	 
	}
	 
	}
	 
	System.out.println();
	 
	}
	}
	}

